var Container = require('./container.js');

var Tester = function(ServiceConstructor, options) {
  this._container = new Container(ServiceConstructor, options);
  this.service = this._container._service;
};

Tester.prototype.start = function() {
  return this._container._start();
};

Tester.prototype.stop = function() {
  return this._container._stop();
};

module.exports = Tester;
